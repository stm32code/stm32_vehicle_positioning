#include "heartiic.h"
#include "delay.h"

// 初始化IIC
void heart_IIC_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(DATA_GPIO_CLK, ENABLE);
	GPIO_InitStructure.GPIO_Pin = DATA__GPIO_PIN; // PC.11  CE
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; // 推挽输出
	GPIO_Init(DATA__GPIO_PORT, &GPIO_InitStructure); // 初始化GPIOC.11

	GPIO_InitStructure.GPIO_Pin = SCLK__GPIO_PIN; // PC.12  SCLK
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; // 推挽输出
	GPIO_Init(SCLK__GPIO_PORT, &GPIO_InitStructure); // 初始化GPIOC.12

	Heart_IIC_SCL = 1;
	Heart_IIC_SDA = 1;
}
// 配置双向I/O端口为输出态
static void Heart_SDA_OUT()
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(DATA_GPIO_CLK, ENABLE);

	GPIO_InitStructure.GPIO_Pin = DATA__GPIO_PIN; // PC.10  DATA
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_Init(DATA__GPIO_PORT, &GPIO_InitStructure); // 初始化GPIOC.10
}
// 配置双向I/O端口为输入态
static void Heart_SDA_IN()
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(DATA_GPIO_CLK, ENABLE);
	GPIO_InitStructure.GPIO_Pin = DATA__GPIO_PIN; // PC.10 DATA
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;
	GPIO_Init(DATA__GPIO_PORT, &GPIO_InitStructure); // 初始化GPIOC.10
}

// 产生IIC起始信号
void heart_IIC_Start(void)
{
	Heart_SDA_OUT(); // sda线输出
	Heart_IIC_SDA = 1;
	Heart_IIC_SCL = 1;
	delay_us(4);
	Heart_IIC_SDA = 0; // START:when CLK is high,DATA change form high to low
	delay_us(4);
	Heart_IIC_SCL = 0; // 钳住I2C总线，准备发送或接收数据
}
// 产生IIC停止信号
void heart_IIC_Stop(void)
{
	Heart_SDA_OUT(); // sda线输出
	Heart_IIC_SCL = 0;
	Heart_IIC_SDA = 0; // STOP:when CLK is high DATA change form low to high
	delay_us(4);
	Heart_IIC_SCL = 1;
	Heart_IIC_SDA = 1; // 发送I2C总线结束信号
	delay_us(4);
}
// 等待应答信号到来
// 返回值：1，接收应答失败
//         0，接收应答成功
u8 heart_IIC_Wait_Ack(void)
{
	u8 ucErrTime = 0;
	Heart_SDA_IN(); // SDA设置为输入
	Heart_IIC_SDA = 1;
	delay_us(1);
	Heart_IIC_SCL = 1;
	delay_us(1);
	while (Heart_READ_SDA)
	{
		ucErrTime++;
		if (ucErrTime > 250)
		{
			heart_IIC_Stop();
			return 1;
		}
	}
	Heart_IIC_SCL = 0; // 时钟输出0
	return 0;
}
// 产生ACK应答
void heart_IIC_Ack(void)
{
	Heart_IIC_SCL = 0;
	Heart_SDA_OUT();
	Heart_IIC_SDA = 0;
	delay_us(2);
	Heart_IIC_SCL = 1;
	delay_us(2);
	Heart_IIC_SCL = 0;
}
// 不产生ACK应答
void heart_IIC_NAck(void)
{
	Heart_IIC_SCL = 0;
	Heart_SDA_OUT();
	Heart_IIC_SDA = 1;
	delay_us(2);
	Heart_IIC_SCL = 1;
	delay_us(2);
	Heart_IIC_SCL = 0;
}
// IIC发送一个字节
// 返回从机有无应答
// 1，有应答
// 0，无应答
void heart_IIC_Send_Byte(u8 txd)
{
	u8 t;
	Heart_SDA_OUT();
	Heart_IIC_SCL = 0; // 拉低时钟开始数据传输
	for (t = 0; t < 8; t++)
	{
		Heart_IIC_SDA = (txd & 0x80) >> 7;
		txd <<= 1;
		delay_us(2); // 对TEA5767这三个延时都是必须的
		Heart_IIC_SCL = 1;
		delay_us(2);
		Heart_IIC_SCL = 0;
		delay_us(2);
	}
}
// 读1个字节，ack=1时，发送ACK，ack=0，发送nACK
u8 heart_IIC_Read_Byte(unsigned char ack)
{
	unsigned char i, receive = 0;
	Heart_SDA_IN(); // SDA设置为输入
	for (i = 0; i < 8; i++)
	{
		Heart_IIC_SCL = 0;
		delay_us(2);
		Heart_IIC_SCL = 1;
		receive <<= 1;
		if (Heart_READ_SDA)
			receive++;
		delay_us(1);
	}
	if (!ack)
		heart_IIC_NAck(); // 发送nACK
	else
		heart_IIC_Ack(); // 发送ACK
	return receive;
}

void IIC_WriteBytes(u8 WriteAddr, u8 *data, u8 dataLength)
{
	u8 i;
	heart_IIC_Start();

	heart_IIC_Send_Byte(WriteAddr); // 发送写命令
	heart_IIC_Wait_Ack();

	for (i = 0; i < dataLength; i++)
	{
		heart_IIC_Send_Byte(data[i]);
		heart_IIC_Wait_Ack();
	}
	heart_IIC_Stop(); // 产生一个停止条件
	delay_ms(10);
}

void IIC_ReadBytes(u8 deviceAddr, u8 writeAddr, u8 *data, u8 dataLength)
{
	u8 i;
	heart_IIC_Start();

	heart_IIC_Send_Byte(deviceAddr); // 发送写命令
	heart_IIC_Wait_Ack();
	heart_IIC_Send_Byte(writeAddr);
	heart_IIC_Wait_Ack();
	heart_IIC_Send_Byte(deviceAddr | 0X01); // 进入接收模式
	heart_IIC_Wait_Ack();

	for (i = 0; i < dataLength - 1; i++)
	{
		data[i] = heart_IIC_Read_Byte(1);
	}
	data[dataLength - 1] = heart_IIC_Read_Byte(0);
	heart_IIC_Stop(); // 产生一个停止条件
	delay_ms(10);
}

void heart_IIC_Read_One_Byte(u8 daddr, u8 addr, u8 *data)
{
	heart_IIC_Start();

	heart_IIC_Send_Byte(daddr); // 发送写命令
	heart_IIC_Wait_Ack();
	heart_IIC_Send_Byte(addr); // 发送地址
	heart_IIC_Wait_Ack();
	heart_IIC_Start();
	heart_IIC_Send_Byte(daddr | 0X01); // 进入接收模式
	heart_IIC_Wait_Ack();
	*data = heart_IIC_Read_Byte(0);
	heart_IIC_Stop(); // 产生一个停止条件
}

void heart_IIC_Write_One_Byte(u8 daddr, u8 addr, u8 data)
{
	heart_IIC_Start();

	heart_IIC_Send_Byte(daddr); // 发送写命令
	heart_IIC_Wait_Ack();
	heart_IIC_Send_Byte(addr); // 发送地址
	heart_IIC_Wait_Ack();
	heart_IIC_Send_Byte(data); // 发送字节
	heart_IIC_Wait_Ack();
	heart_IIC_Stop(); // 产生一个停止条件
	delay_ms(10);
}
